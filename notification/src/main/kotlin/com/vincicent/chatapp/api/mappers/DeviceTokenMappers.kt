package com.vincicent.chatapp.api.mappers

import com.vincicent.chatapp.api.dto.DeviceTokenDto
import com.vincicent.chatapp.api.dto.PlatformDto
import com.vincicent.chatapp.domain.model.DeviceToken

fun DeviceToken.toDeviceTokenDto(): DeviceTokenDto {
    return DeviceTokenDto(
        userId = userId,
        token = token,
        createdAt = createdAt
    )
}

fun PlatformDto.toPlatformDto(): DeviceToken.Platform {
    return when(this) {
        PlatformDto.ANDROID -> DeviceToken.Platform.ANDROID
        PlatformDto.IOS -> DeviceToken.Platform.IOS
    }
}